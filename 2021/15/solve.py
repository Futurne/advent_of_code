from itertools import product
from collections import deque

import numpy as np


class Node:
    def __init__(self, cell: tuple, value: int, neightbours: list):
        self.cell = cell
        self.value = value
        self.neightbours = neightbours

        self.total_cost = None
        self.next_node = None

    def update_cost(self) -> bool:
        has_changed = False

        for node in self.neightbours:
            if node.total_cost is None:
                continue  # No path to this node yet

            if self.total_cost is None:
                self.total_cost = self.value + node.total_cost
                self.next_node = node
                has_changed = True
            elif self.total_cost > self.value + node.total_cost:
                self.total_cost = self.value + node.total_cost
                self.next_node = node
                has_changed = True

        return has_changed


def build_graph(matrix: np.array, end_cell: tuple) -> dict:
    graph = dict()

    # Build nodes
    for x, y in product(range(matrix.shape[0]), range(matrix.shape[1])):
        graph[(x, y)] = Node((x, y), matrix[x, y], None)

    # Update neightbours
    for (x, y), node in graph.items():
        neightbours = [
            (a, b) for (a, b) in [(x+1, y), (x-1, y), (x, y-1), (x, y+1)]
            if 0 <= a < matrix.shape[0] and 0 <= b < matrix.shape[1]
        ]

        neightbours = [graph[n] for n in neightbours]
        node.neightbours = neightbours

    graph[end_cell].total_cost = matrix[end_cell]
    return graph


def update_all_nodes(graph: dict, end_cell: tuple) -> bool:
    has_changed = False
    queue = deque()  # FIFO
    queue.append(graph[end_cell])
    visited = set()
    visited.add(end_cell)

    while queue:
        node = queue.popleft()
        has_changed = node.update_cost() or has_changed

        for n in node.neightbours:
            if n.cell not in visited:
                queue.append(n)
                visited.add(n.cell)

    return has_changed


def find_best_path(matrix: np.array) -> dict:
    end_cell = (matrix.shape[0] - 1, matrix.shape[1] - 1)
    graph = build_graph(matrix, end_cell)

    while update_all_nodes(graph, end_cell):
        continue

    return graph


def input_to_matrix(lines: list[str]) -> np.array:
    lines = [l.replace('\n', '') for l in lines]
    matrix = [
        [
            int(n)
            for n in l
        ]
        for l in lines
    ]
    return np.array(matrix)


def expand_matrix(matrix: np.array) -> np.array:
    factor = 5
    width, height = matrix.shape
    big_matrix = np.zeros((width * factor, height * factor), dtype=int)

    for x, y in product(range(factor), range(factor)):
        n_matrix = matrix + np.ones(matrix.shape) * (x + y)
        n_matrix[n_matrix > 9] = n_matrix[n_matrix > 9] % 9
        big_matrix[x*width:(x+1)*width, y*height:(y+1)*height] = n_matrix

    return big_matrix


def solve(input_path: str, expand: bool) -> int:
    with open(input_path, 'r') as input_file:
        lines = input_file.readlines()
        matrix = input_to_matrix(lines)

    if expand:
        matrix = expand_matrix(matrix)

    graph = find_best_path(matrix)
    cost = graph[(0, 0)].total_cost
    cost -= matrix[0, 0]  # Don't count the first cell cost
    return cost


